async function main() {
  const SUPABASE_URL = process.env.SUPABASE_URL || process.env.VITE_SUPABASE_URL;
  const SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!SUPABASE_URL || !SERVICE_KEY) {
    console.error('Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY');
    process.exit(1);
  }

  const { createClient } = await import('@supabase/supabase-js');
  const supabase = createClient(SUPABASE_URL, SERVICE_KEY);

  const now = new Date().toISOString();

  for (const post of POSTS) {
    const slug = createSlug(post.title).slice(0, 60).replace(/-+$/, '');
    const payload = {
      title: post.title,
      slug,
      content: post.content.trim(),
      excerpt: post.excerpt,
      featured_image: post.featured_image,
      meta_title: post.meta_title,
      meta_description: post.meta_description,
      meta_keywords: post.meta_keywords,
      author_id: AUTHOR_ID,
      status: 'published',
      is_featured: false,
      published_at: now,
    };

    const linkCount = (post.content.match(/<a /g) || []).length;

    // Upsert by slug — idempotent.
    const { data: existing } = await supabase
      .from('blog_posts')
      .select('id')
      .eq('slug', slug)
      .maybeSingle();

    if (existing) {
      const { error } = await supabase.from('blog_posts').update(payload).eq('id', existing.id);
      if (error) {
        console.error(`✗ update ${slug}:`, error.message);
      } else {
        console.log(`↻ updated ${slug}  (${linkCount} inline links)`);
      }
    } else {
      const { error } = await supabase.from('blog_posts').insert(payload);
      if (error) {
        console.error(`✗ insert ${slug}:`, error.message);
      } else {
        console.log(`✓ inserted ${slug}  (${linkCount} inline links)`);
      }
    }
  }

  console.log('\nSeeding complete.');
}

main().catch((err) => {
  console.error('Fatal:', err);
  process.exit(1);
});
